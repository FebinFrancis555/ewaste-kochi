export const getLocationBySlug = (slug) => locations.find(l => l.slug === slug);

export const getNearbyLocations = (slug, limit = 5) => {
  const location = getLocationBySlug(slug);
  if (!location) return [];
  return location.nearbyAreas
    .map(s => getLocationBySlug(s))
    .filter(Boolean)
    .slice(0, limit);
};
